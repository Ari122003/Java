package Basics;

import java.util.*;

public class Input_Sum {

    public static void main(String[] args) {

        int a, b;

        System.out.println("Enter first no: ");

        Scanner scanner = new Scanner(System.in);

        a = scanner.nextInt();

        System.out.println("Enter second no: ");

        b = scanner.nextInt();

        System.out.println("Sum is : " + (a + b));

    }

}
