package Primitive_Datatypes;

public class Wrapper_Classes {
    // Primitive data types
        byte b = 10;
        short s = 20;
        int i = 30;
        long l = 40L;
        float f = 3.14f;
        double d = 99.99;
        char c = 'A';
        boolean bool = true;

        // Wrapper classes
        Byte wb = Byte.valueOf(b);
        Short ws = Short.valueOf(s);
        Integer wi = Integer.valueOf(i);
        Long wl = Long.valueOf(l);
        Float wf = Float.valueOf(f);
        Double wd = Double.valueOf(d);
        Character wc = Character.valueOf(c);
        Boolean wbool = Boolean.valueOf(bool);
}
